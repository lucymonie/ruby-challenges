millie_my_tweet = [
	"You are so small that your existence could be measured in eyeblinks", 
	"Yet it is a lifetime since you arrived. Your lifetime.",
	"It's hard to remember that I once had a life without you in it,",
	"You're like a song that already existed, waiting to be written down."
]

total_poem = millie_my_tweet.size
lines_putsed = 0

while (lines_putsed < total_poem)
	puts millie_my_tweet[lines_putsed]
	lines_putsed += 1
end