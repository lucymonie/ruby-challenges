weather = "windy"
weather_is = "The weather is #{weather}"
case weather
	when "rainy" then puts "#{weather_is}, so layer up and bring an umbrella."
	when "sunny" then puts "#{weather_is}, so bring your sunglasses and a sweater."
	when "snowing" then puts "#{weather_is}, so you'll need a nice big coat and some gloves."
	when "hot" then puts "#{weather_is}, so bust out the summer clothes!"
	when "windy" then puts "#{weather_is}, so tie back your hair and batten down the hatches."
	end
